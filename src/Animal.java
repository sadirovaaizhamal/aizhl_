public class Animal {
 private int age;
    private Color color;
    private int weight;

    public Animal(int age, Color color, int weight) {
        this.age = age;
        this.color = color;
        this.weight = weight;

    }



    public int getAge() {
        return age;
    }

    public Color getColor() {
        return color;
    }

    public int getWeight() {
        return weight;
    }
}
